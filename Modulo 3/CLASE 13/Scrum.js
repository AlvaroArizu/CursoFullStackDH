/*
SCRUM

Es una metodologia de desarrolo agil mas usada en el mundo
Es una herrameinta que FOMENTA EL TRABAJO EN EQUIPO!!

Tiene 3 roles funadamentales dentro del equipo:

1)Product Owner: Representa la voz del cliente y es quien toma las descsiones filanes del producto. Es quien se asegura que el equipo trabaja de forma adecuada desde la perspectiva del negocio, entre otras cosas.

2)Scrum Master: Es un facilitaodr cuyo principal enfoque es asegurar el intercambio efeciente de la info, entre otras cosas.

3)Equipo de desarrollo o equipo tecnico: Conformado de 3 a 9 personas que deberan contar con todas las capacidades tecnicas requeridad para entregar el producto desarrolado.

4)Stakeholders o accionistas: Son las personas que utilizaran o seran afectadas por el producto desarrolado 

Daily Stan-up(Es como una reunion):
-Dura 3m por persona y contesta 3 preguntas

 -Que hice ayer?
 -Tuve impedimentos para lograr mis objetivos?
 -Que voy a hacer hoy?

Revision o Demo: el objetivo es revizar el trabajo completado y luego presentarlo

Retrospectiva: es un reunion enfocada en la mejora continua del proceso y cominicacion del equipo 


*/
